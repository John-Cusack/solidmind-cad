# Result parsing
# ---------------------------------------------------------------------------


def parse_su2_history(
    work_dir: Path,
    spec: AnalysisSpec,
) -> AeroResult:
    """Parse SU2 history.csv for final converged aerodynamic coefficients."""
    history_path = work_dir / "history.csv"

    if not history_path.exists():
        return _failed_aero_result("No history.csv found — solver may have failed")

    # Read last row of history.csv
    try:
        with open(history_path) as f:
            reader = csv.DictReader(f)
            rows = list(reader)

        if not rows:
            return _failed_aero_result("history.csv is empty")

        last = rows[-1]

        # SU2 column names vary by solver type — try common variants
        cl = _get_coeff(last, "CL", '"CL"', "CD[cl]", "Lift")
        cd = _get_coeff(last, "CD", '"CD"', "CD[cd]", "Drag")
        cs = _get_coeff(last, "CSF", '"CSF"', "CS")
        cmx = _get_coeff(last, "CMx", '"CMx"')
        cmy = _get_coeff(last, "CMy", '"CMy"')
        cmz = _get_coeff(last, "CMz", '"CMz"')

    except Exception as exc:
        return _failed_aero_result(f"Failed to parse history.csv: {exc}")

    # Compute dimensional forces
    flow = FlowConditions(velocity_m_s=15.0)  # default
    ref = AeroReference(area_m2=1.0)
    for bc in spec.boundary_conditions:
        if bc.bc_type == "freestream":
            flow = FlowConditions.from_dict(bc.value)
        elif bc.bc_type == "reference":
            ref = AeroReference.from_dict(bc.value)

    q = 0.5 * flow.density_kg_m3 * flow.velocity_m_s**2  # dynamic pressure
    lift_n = cl * q * ref.area_m2
    drag_n = cd * q * ref.area_m2
    l_over_d = cl / cd if abs(cd) > 1e-12 else 0.0

    # Build checks
    checks = _build_aero_checks(cl, cd, l_over_d, lift_n, drag_n)

    overall = CheckStatus.PASS
    for c in checks:
        if c.status == CheckStatus.FAIL:
            overall = CheckStatus.FAIL
            break
        if c.status == CheckStatus.WARN:
            overall = CheckStatus.WARN

    return AeroResult(
        analysis_id="",
        status=overall,
        cl=round(cl, 6),
        cd=round(cd, 6),
        cs=round(cs, 6),
        cmx=round(cmx, 6),
        cmy=round(cmy, 6),
        cmz=round(cmz, 6),
        l_over_d=round(l_over_d, 2),
        lift_n=round(lift_n, 4),
        drag_n=round(drag_n, 4),
        checks=tuple(checks),
        solver_name="su2",
    )


def _get_coeff(row: dict[str, str], *keys: str) -> float:
    """Try multiple column name variants to extract a coefficient."""
    for key in keys:
        # Strip whitespace from CSV column names
        for col_name, val in row.items():
            if col_name.strip().strip('"') == key.strip().strip('"'):
                try:
                    return float(val)
                except ValueError:
                    continue
    return 0.0


def _build_aero_checks(
    cl: float,
    cd: float,
    l_over_d: float,
    lift_n: float,
    drag_n: float,
) -> list[AnalysisCheck]:
    """Build aerodynamic quality checks."""
    checks: list[AnalysisCheck] = []

    # L/D ratio check
    if l_over_d < 3:
        checks.append(
            AnalysisCheck(
                name="l_over_d",
                status=CheckStatus.FAIL,
                message=f"L/D = {l_over_d:.1f} is very low (typical drone: 5-15)",
                measured=l_over_d,
                limit=5.0,
                suggestion="Reduce airframe drag or increase wing/body lift. Check for flow separation.",
            )
        )
    elif l_over_d < 5:
        checks.append(
            AnalysisCheck(
                name="l_over_d",
                status=CheckStatus.WARN,
                message=f"L/D = {l_over_d:.1f} is below typical drone range (5-15)",
                measured=l_over_d,
                limit=5.0,
                suggestion="Consider streamlining the airframe or increasing aspect ratio.",
            )
        )
    else:
        checks.append(
            AnalysisCheck(
                name="l_over_d",
                status=CheckStatus.PASS,
                message=f"L/D = {l_over_d:.1f}",
                measured=l_over_d,
            )
        )

    # Drag coefficient check
    if cd > 0.1:
        checks.append(
            AnalysisCheck(
                name="drag_coefficient",
                status=CheckStatus.WARN,
                message=f"CD = {cd:.4f} is high — check for separation or bluff body drag",
                measured=cd,
                limit=0.1,
                suggestion="Streamline the geometry, add fairings, or reduce frontal area.",
            )
        )
    else:
        checks.append(
            AnalysisCheck(
                name="drag_coefficient",
                status=CheckStatus.PASS,
                message=f"CD = {cd:.4f}",
                measured=cd,
            )
        )

    # Negative lift check (for multicopter bodies, small negative is OK)
    if cl < -0.5:
        checks.append(
            AnalysisCheck(
                name="lift_coefficient",
                status=CheckStatus.WARN,
                message=f"CL = {cl:.4f} — significant downforce",
                measured=cl,
                suggestion="Check angle of attack and body orientation.",
            )
        )
    else:
        checks.append(
            AnalysisCheck(
                name="lift_coefficient",
                status=CheckStatus.PASS,
                message=f"CL = {cl:.4f}",
                measured=cl,
            )
        )

    return checks


def _failed_aero_result(message: str) -> AeroResult:
    return AeroResult(
        analysis_id="",
        status=CheckStatus.FAIL,
        cl=0.0,
        cd=0.0,
        cs=0.0,
        cmx=0.0,
        cmy=0.0,
        cmz=0.0,
        l_over_d=0.0,
        lift_n=0.0,
        drag_n=0.0,
        checks=(
            AnalysisCheck(
                name="solver_error",
                status=CheckStatus.FAIL,
                message=message,
                suggestion="Check solver installation and input files",
            ),
        ),
    )
