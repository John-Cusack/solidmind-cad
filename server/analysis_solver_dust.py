"""DUST vortex particle method solver for multi-rotor aerodynamic analysis.

DUST is an open-source unsteady aerodynamics tool using vortex particle methods.
It excels at multi-rotor wake interaction analysis — computing thrust, torque,
and efficiency for overlapping/coaxial rotor configurations at a fraction of
the cost of mesh-based CFD.

Binary: ``dust``.  Install from source:
  https://github.com/Music-and-Culture-Technology-Lab/dust

DUST uses surface panel geometry (not volume mesh), making it much faster
than RANS CFD for rotor-on-rotor interaction studies.
"""
from __future__ import annotations

import json
import logging
import math
import shutil
import subprocess
import time
from pathlib import Path
from typing import Any

from server.analysis_models import (
    AeroReference,
    AeroResult,
    AnalysisCheck,
    AnalysisSpec,
    AnalysisType,
    CheckStatus,
    FieldResult,
    FlowConditions,
    MeshInfo,
    RotorSpec,
    ScalarFieldSummary,
)
from server.analysis_solvers import FieldSolver

log = logging.getLogger("solidmind.solver_dust")


class DUSTSolver(FieldSolver):
    """DUST vortex particle method solver for multi-rotor analysis."""

    def name(self) -> str:
        return "dust"

    def analysis_types(self) -> list[AnalysisType]:
        return [AnalysisType.AERODYNAMIC]

    def available(self) -> tuple[bool, str]:
        binary = shutil.which("dust")
        if binary:
            return True, f"dust found at {binary}"
        return False, (
            "dust not found. Install from source:\n"
            "  https://github.com/Music-and-Culture-Technology-Lab/dust\n"
            "  or use SU2 for single-body aerodynamics."
        )

    def write_input(
        self,
        spec: AnalysisSpec,
        mesh_info: MeshInfo,
        work_dir: Path,
    ) -> Path:
        """Generate DUST input files.

        DUST uses a main configuration file that references geometry components
        and defines simulation parameters.
        """
        config_path = work_dir / "dust.in"

        flow = self._extract_flow_conditions(spec)
        ref = self._extract_aero_reference(spec)
        rotors = self._extract_rotors(spec)

        # Convert mesh to surface panels (STL → DUST component format)
        self._prepare_geometry(mesh_info, work_dir, spec.body)

        # Compute time stepping for unsteady analysis
        if rotors:
            max_rpm = max(r.rpm for r in rotors)
            # 10 degrees per step → 36 steps per revolution
            omega = max_rpm * 2 * math.pi / 60
            dt = (10 * math.pi / 180) / omega if omega > 0 else 0.01
            n_steps = max(360, int(2 * 360))  # 2 full revolutions
        else:
            dt = 0.01
            n_steps = 100

        lines: list[str] = []
        lines.append("! DUST configuration generated by SolidMind CAD")
        lines.append(f"! Body: {spec.body}")
        lines.append("")

        # Reference values
        lines.append("&reference_parameters")
        lines.append(f"  u_inf = {flow.velocity_m_s}")
        lines.append(f"  rho_inf = {flow.density_kg_m3}")
        lines.append(f"  a_inf = 340.3")
        lines.append(f"  mu_inf = {flow.viscosity_pa_s}")
        lines.append(f"  ref_area = {ref.area_m2 if ref.area_m2 > 0 else 1.0}")
        lines.append(f"  ref_length = {ref.chord_m if ref.chord_m > 0 else 1.0}")
        lines.append("/")
        lines.append("")

        # Freestream
        aoa_rad = math.radians(flow.angle_of_attack_deg)
        beta_rad = math.radians(flow.sideslip_deg)
        lines.append("&freestream")
        lines.append(f"  u_inf_x = {flow.velocity_m_s * math.cos(aoa_rad) * math.cos(beta_rad):.6f}")
        lines.append(f"  u_inf_y = {flow.velocity_m_s * math.sin(beta_rad):.6f}")
        lines.append(f"  u_inf_z = {flow.velocity_m_s * math.sin(aoa_rad) * math.cos(beta_rad):.6f}")
        lines.append("/")
        lines.append("")

        # Time stepping
        lines.append("&time_parameters")
        lines.append(f"  dt = {dt:.6f}")
        lines.append(f"  n_timesteps = {n_steps}")
        lines.append(f"  start_output = {n_steps // 2}")  # skip transient
        lines.append("/")
        lines.append("")

        # Wake parameters
        lines.append("&wake_parameters")
        lines.append("  n_wake_panels = 50")
        lines.append("  wake_free_length = 5.0")
        lines.append("  vortex_rad = 0.02")
        lines.append("/")
        lines.append("")

        # Components — airframe body
        lines.append("&component")
        lines.append(f"  comp_name = '{spec.body}'")
        lines.append(f"  geo_file = 'body.stl'")
        lines.append("  comp_type = 'surface_panel'")
        lines.append("  trailing_edge = .false.")
        lines.append("/")
        lines.append("")

        # Rotor components
        for rotor in rotors:
            omega_rad_s = rotor.rpm * 2 * math.pi / 60
            cx, cy, cz = rotor.center_xyz
            ax, ay, az = rotor.axis
            lines.append("&component")
            lines.append(f"  comp_name = '{rotor.rotor_id}'")
            lines.append(f"  comp_type = 'lifting_line_rotor'")
            lines.append(f"  n_blades = {rotor.num_blades}")
            lines.append(f"  radius = {rotor.radius_m}")
            lines.append(f"  omega = {omega_rad_s:.4f}")
            lines.append(f"  hub_center = {cx}, {cy}, {cz}")
            lines.append(f"  rotation_axis = {ax}, {ay}, {az}")
            lines.append(f"  collective = {rotor.collective_deg}")
            if rotor.chord_m > 0:
                lines.append(f"  chord = {rotor.chord_m}")
            lines.append("/")
            lines.append("")

        # Output
        lines.append("&output")
        lines.append("  output_forces = .true.")
        lines.append("  output_moments = .true.")
        lines.append("  output_format = 'csv'")
        lines.append("  output_dir = 'results'")
        lines.append("/")

        config_path.write_text("\n".join(lines) + "\n")
        return config_path

    def run(self, input_path: Path, work_dir: Path) -> float:
        """Run DUST solver."""
        # Create results directory
        (work_dir / "results").mkdir(exist_ok=True)

        t0 = time.monotonic()
        result = subprocess.run(
            ["dust", input_path.name],
            cwd=str(work_dir),
            capture_output=True,
            text=True,
            timeout=600,
        )
        elapsed = time.monotonic() - t0

        if result.returncode != 0:
            log.error("dust failed (rc=%d): %s", result.returncode, result.stderr[:500])
            raise RuntimeError(
                f"DUST failed (rc={result.returncode}): {result.stderr[:500]}"
            )

        return elapsed

    def parse_results(
        self,
        work_dir: Path,
        spec: AnalysisSpec,
    ) -> FieldResult:
        """Parse DUST output for aerodynamic forces per component."""
        aero = parse_dust_results(work_dir, spec)

        return FieldResult(
            analysis_id=aero.analysis_id,
            status=aero.status,
            safety_factor=aero.l_over_d,
            max_von_mises_mpa=0.0,
            max_displacement_mm=0.0,
            checks=aero.checks,
            scalar_fields=(),
            solver_name="dust",
            solve_time_s=aero.solve_time_s,
        )

    def parse_aero_results(
        self,
        work_dir: Path,
        spec: AnalysisSpec,
    ) -> AeroResult:
        """Return the full AeroResult with rotor_forces."""
        return parse_dust_results(work_dir, spec)

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def _prepare_geometry(
        self,
        mesh_info: MeshInfo,
        work_dir: Path,
        body_name: str,
    ) -> None:
        """Convert volume mesh to surface STL for DUST panel method."""
        try:
            import meshio
            mesh = meshio.read(mesh_info.path)
            meshio.write(str(work_dir / "body.stl"), mesh, file_format="stl")
        except ImportError:
            log.warning("meshio not installed — writing stub geometry")
            (work_dir / "body.stl").write_text("solid stub\nendsolid stub\n")
        except Exception as exc:
            log.warning("STL conversion failed: %s", exc)
            (work_dir / "body.stl").write_text("solid stub\nendsolid stub\n")

    def _extract_flow_conditions(self, spec: AnalysisSpec) -> FlowConditions:
        for bc in spec.boundary_conditions:
            if bc.bc_type == "freestream":
                return FlowConditions.from_dict(bc.value)
        return FlowConditions(velocity_m_s=0.0)  # hover by default

    def _extract_aero_reference(self, spec: AnalysisSpec) -> AeroReference:
        for bc in spec.boundary_conditions:
            if bc.bc_type == "reference":
                return AeroReference.from_dict(bc.value)
        return AeroReference(area_m2=1.0, chord_m=0.1)

    def _extract_rotors(self, spec: AnalysisSpec) -> list[RotorSpec]:
        rotors: list[RotorSpec] = []
        for bc in spec.boundary_conditions:
            if bc.bc_type == "rotor":
                rotors.append(RotorSpec.from_dict(bc.value))
        return rotors


