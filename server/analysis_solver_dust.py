class MockDUSTSolver(FieldSolver):
    """Deterministic mock DUST solver for testing multi-rotor analysis."""

    def name(self) -> str:
        return "mock_dust"

    def analysis_types(self) -> list[AnalysisType]:
        return [AnalysisType.AERODYNAMIC]

    def available(self) -> tuple[bool, str]:
        return True, "mock DUST solver (always available)"

    def write_input(self, spec: AnalysisSpec, mesh_info: MeshInfo, work_dir: Path) -> Path:
        p = work_dir / "mock_dust.in"
        p.write_text("mock dust input\n")
        return p

    def run(self, input_path: Path, work_dir: Path) -> float:
        (work_dir / "results").mkdir(exist_ok=True)
        return 0.05

    def _compute_aero(self, spec: AnalysisSpec) -> AeroResult:
        """Compute deterministic mock aero result from rotor specs."""
        rotors = []
        for bc in spec.boundary_conditions:
            if bc.bc_type == "rotor":
                rotors.append(RotorSpec.from_dict(bc.value))

        rotor_forces: dict[str, dict[str, float]] = {}
        total_thrust = 0.0
        total_power = 0.0
        for rotor in rotors:
            # Simple momentum theory: T = CT * rho * A * (omega*R)^2
            area = math.pi * rotor.radius_m ** 2
            omega = rotor.rpm * 2 * math.pi / 60
            tip_speed = omega * rotor.radius_m
            rho = 1.225
            ct = 0.008  # typical
            cq = 0.0006  # typical
            thrust = ct * rho * area * tip_speed ** 2
            torque = cq * rho * area * tip_speed ** 2 * rotor.radius_m
            power = torque * omega
            total_thrust += thrust
            total_power += power
            rotor_forces[rotor.rotor_id] = {
                "thrust_n": round(thrust, 4),
                "torque_nm": round(torque, 4),
                "power_w": round(power, 4),
                "ct": ct,
                "cq": cq,
            }

        cl = 0.01  # near-zero for hover
        cd = 0.02
        l_over_d = cl / cd if cd > 0 else 0.0

        checks = _build_rotor_checks(rotor_forces, total_thrust, total_power, rotors)

        return AeroResult(
            analysis_id="mock_dust_001",
            status=checks[0].status if checks else CheckStatus.PASS,
            cl=cl, cd=cd, cs=0.0,
            cmx=0.0, cmy=0.0, cmz=0.0,
            l_over_d=round(l_over_d, 2),
            lift_n=round(total_thrust, 4),
            drag_n=0.0,
            checks=tuple(checks),
            rotor_forces=rotor_forces,
            solver_name="mock_dust",
            solve_time_s=0.05,
        )

    def parse_results(self, work_dir: Path, spec: AnalysisSpec) -> FieldResult:
        aero = self._compute_aero(spec)
        return FieldResult(
            analysis_id=aero.analysis_id,
            status=aero.status,
            safety_factor=aero.l_over_d,
            max_von_mises_mpa=0.0,
            max_displacement_mm=0.0,
            checks=aero.checks,
            scalar_fields=(),
            solver_name="mock_dust",
            solve_time_s=0.05,
        )

    def parse_aero_results(self, work_dir: Path, spec: AnalysisSpec) -> AeroResult:
        """Return the full AeroResult with rotor_forces."""
        return self._compute_aero(spec)


