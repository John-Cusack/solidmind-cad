def parse_dust_results(
    work_dir: Path,
    spec: AnalysisSpec,
) -> AeroResult:
    """Parse DUST output files for aerodynamic forces and rotor performance."""
    results_dir = work_dir / "results"

    # Look for force output files
    force_files = sorted(results_dir.glob("*forces*.csv")) if results_dir.exists() else []

    if not force_files:
        return _failed_aero_result("No force output files found in results/")

    # Parse time-averaged forces from the last portion of simulation
    total_cl = 0.0
    total_cd = 0.0
    total_lift = 0.0
    total_drag = 0.0
    rotor_forces: dict[str, dict[str, float]] = {}

    try:
        for ff in force_files:
            import csv
            with open(ff) as f:
                reader = csv.DictReader(f)
                rows = list(reader)
            if not rows:
                continue
            # Average last quarter of data (skip transient)
            n_avg = max(1, len(rows) // 4)
            last_rows = rows[-n_avg:]

            comp_name = ff.stem.replace("_forces", "").replace("forces_", "")

            # Accumulate coefficients
            for key_cl, key_cd in [("CL", "CD"), ("Cl", "Cd"), ("cl", "cd")]:
                if key_cl in last_rows[0]:
                    avg_cl = sum(float(r.get(key_cl, 0)) for r in last_rows) / n_avg
                    avg_cd = sum(float(r.get(key_cd, 0)) for r in last_rows) / n_avg
                    total_cl += avg_cl
                    total_cd += avg_cd
                    break

            # Check for rotor-specific outputs (thrust, torque)
            for key_t in ("thrust", "Thrust", "T"):
                if key_t in last_rows[0]:
                    avg_t = sum(float(r.get(key_t, 0)) for r in last_rows) / n_avg
                    avg_q = 0.0
                    for key_q in ("torque", "Torque", "Q"):
                        if key_q in last_rows[0]:
                            avg_q = sum(float(r.get(key_q, 0)) for r in last_rows) / n_avg
                            break
                    rotor_forces[comp_name] = {
                        "thrust_n": round(avg_t, 4),
                        "torque_nm": round(avg_q, 4),
                    }
                    total_lift += avg_t
                    break

    except Exception as exc:
        return _failed_aero_result(f"Failed to parse DUST results: {exc}")

    l_over_d = total_cl / total_cd if abs(total_cd) > 1e-12 else 0.0

    # Extract rotor specs for checks
    rotors = [
        RotorSpec.from_dict(bc.value)
        for bc in spec.boundary_conditions
        if bc.bc_type == "rotor"
    ]

    checks = _build_rotor_checks(
        rotor_forces, total_lift, 0.0, rotors,
    )

    overall = CheckStatus.PASS
    for c in checks:
        if c.status == CheckStatus.FAIL:
            overall = CheckStatus.FAIL
            break
        if c.status == CheckStatus.WARN:
            overall = CheckStatus.WARN

    return AeroResult(
        analysis_id="",
        status=overall,
        cl=round(total_cl, 6),
        cd=round(total_cd, 6),
        cs=0.0, cmx=0.0, cmy=0.0, cmz=0.0,
        l_over_d=round(l_over_d, 2),
        lift_n=round(total_lift, 4),
        drag_n=round(total_drag, 4),
        checks=tuple(checks),
        rotor_forces=rotor_forces,
        solver_name="dust",
    )


def _build_rotor_checks(
    rotor_forces: dict[str, dict[str, float]],
    total_thrust: float,
    total_power: float,
    rotors: list[RotorSpec],
) -> list[AnalysisCheck]:
    """Build checks for multi-rotor performance."""
    checks: list[AnalysisCheck] = []

    if not rotor_forces and not rotors:
        checks.append(AnalysisCheck(
            name="aero_result",
            status=CheckStatus.PASS,
            message="Aerodynamic analysis complete (no rotors defined)",
        ))
        return checks

    # Total thrust check
    if total_thrust > 0:
        checks.append(AnalysisCheck(
            name="total_thrust",
            status=CheckStatus.PASS,
            message=f"Total thrust: {total_thrust:.2f} N",
            measured=total_thrust,
        ))
    elif rotors:
        checks.append(AnalysisCheck(
            name="total_thrust",
            status=CheckStatus.WARN,
            message="No thrust produced — check rotor RPM and collective angle",
            measured=0.0,
            suggestion="Increase collective pitch or RPM.",
        ))

    # Per-rotor thrust balance (for multi-rotors)
    if len(rotor_forces) > 1:
        thrusts = [rf.get("thrust_n", 0) for rf in rotor_forces.values()]
        if thrusts:
            mean_t = sum(thrusts) / len(thrusts)
            if mean_t > 0:
                max_imbalance = max(abs(t - mean_t) / mean_t for t in thrusts)
                if max_imbalance > 0.15:
                    checks.append(AnalysisCheck(
                        name="thrust_balance",
                        status=CheckStatus.WARN,
                        message=f"Rotor thrust imbalance: {max_imbalance*100:.1f}% — wake interaction may be causing asymmetry",
                        measured=max_imbalance,
                        limit=0.15,
                        suggestion="Adjust rotor spacing or add tilt to reduce wake impingement.",
                    ))
                else:
                    checks.append(AnalysisCheck(
                        name="thrust_balance",
                        status=CheckStatus.PASS,
                        message=f"Rotor thrust balance: {max_imbalance*100:.1f}% imbalance",
                        measured=max_imbalance,
                    ))

    # Figure of merit (hover efficiency) for each rotor
    for rotor in rotors:
        rf = rotor_forces.get(rotor.rotor_id, {})
        thrust = rf.get("thrust_n", 0)
        power = rf.get("power_w", 0)
        if thrust > 0 and power > 0:
            # FM = T * sqrt(T / (2*rho*A)) / P
            rho = 1.225
            area = math.pi * rotor.radius_m ** 2
            p_ideal = thrust * math.sqrt(thrust / (2 * rho * area))
            fm = p_ideal / power if power > 0 else 0
            if fm < 0.5:
                checks.append(AnalysisCheck(
                    name=f"figure_of_merit_{rotor.rotor_id}",
                    status=CheckStatus.WARN,
                    message=f"Rotor {rotor.rotor_id}: FM = {fm:.3f} (poor, typical > 0.6)",
                    measured=fm,
                    limit=0.6,
                    suggestion="Optimize blade twist/chord distribution or reduce tip losses.",
                ))
            else:
                checks.append(AnalysisCheck(
                    name=f"figure_of_merit_{rotor.rotor_id}",
                    status=CheckStatus.PASS,
                    message=f"Rotor {rotor.rotor_id}: FM = {fm:.3f}",
                    measured=fm,
                ))

    return checks


def _failed_aero_result(message: str) -> AeroResult:
    return AeroResult(
        analysis_id="",
        status=CheckStatus.FAIL,
        cl=0.0, cd=0.0, cs=0.0,
        cmx=0.0, cmy=0.0, cmz=0.0,
        l_over_d=0.0,
        lift_n=0.0, drag_n=0.0,
        checks=(
            AnalysisCheck(
                name="solver_error",
                status=CheckStatus.FAIL,
                message=message,
                suggestion="Check solver installation and input files",
            ),
        ),
    )
