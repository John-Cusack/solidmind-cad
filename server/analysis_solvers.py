# Solver pack discovery
# ---------------------------------------------------------------------------


def _discover_solver_packs() -> None:
    """Load solver packs from ``solidmind.solver_packs`` entry points."""
    for ep in importlib.metadata.entry_points(group="solidmind.solver_packs"):
        try:
            mod = ep.load()
            solvers = getattr(mod, "SOLVERS", [])
            for solver in solvers:
                register_solver(solver)
            log.info(
                "Loaded solver pack %r: %d solvers", ep.name, len(solvers),
            )
        except Exception:
            log.exception("Failed to load solver pack %r", ep.name)


