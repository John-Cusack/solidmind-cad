# Solver registry
# ---------------------------------------------------------------------------

FIELD_SOLVERS: dict[str, FieldSolver] = {}


def register_solver(solver: FieldSolver) -> None:
    """Register a field solver.  Warns on duplicate name."""
    if solver.name() in FIELD_SOLVERS:
        log.warning("Duplicate solver name %r, skipping", solver.name())
        return
    FIELD_SOLVERS[solver.name()] = solver
    log.info("Registered field solver: %s", solver.name())


def get_solver(
    solver_name: str = "",
    analysis_type: AnalysisType = AnalysisType.STRUCTURAL,
    dof_count: int | None = None,
) -> FieldSolver | None:
    """Get a solver by name, or auto-select the first available for the type."""
    if solver_name:
        return FIELD_SOLVERS.get(solver_name)

    if analysis_type == AnalysisType.STRUCTURAL and dof_count is not None:
        cudss_min = _env_int("SOLIDMIND_CUDSS_DOF_MIN", 200_000)
        cholmod_min = _env_int("SOLIDMIND_CHOLMOD_DOF_MIN", 5_000)

        preferred_names: list[str]
        if dof_count >= cudss_min:
            preferred_names = ["cudss", "cholmod", "calculix"]
        elif dof_count >= cholmod_min:
            preferred_names = ["cholmod", "calculix"]
        else:
            preferred_names = ["calculix"]

        for name in preferred_names:
            solver = FIELD_SOLVERS.get(name)
            if solver is None:
                continue
            if analysis_type not in solver.analysis_types():
                continue
            ok, _ = solver.available()
            if ok:
                return solver

    # Auto-select: first available solver that supports the analysis type
    for solver in FIELD_SOLVERS.values():
        if analysis_type in solver.analysis_types():
            ok, _ = solver.available()
            if ok:
                return solver

    return None


def list_solvers() -> list[dict[str, Any]]:
    """List all registered solvers with availability status."""
    result: list[dict[str, Any]] = []
    for solver in FIELD_SOLVERS.values():
        ok, msg = solver.available()
        result.append({
            "name": solver.name(),
            "analysis_types": [t.value for t in solver.analysis_types()],
            "available": ok,
            "diagnostic": msg,
            "supports_direct_solve": solver.supports_direct_solve(),
        })
    return result


def _env_int(name: str, default: int) -> int:
    raw = os.environ.get(name)
    if raw is None:
        return default
    try:
        return int(raw)
    except ValueError:
        log.warning("Invalid %s=%r, using default %d", name, raw, default)
        return default


