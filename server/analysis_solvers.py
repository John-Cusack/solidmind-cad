class MockFieldSolver(FieldSolver):
    """Deterministic mock solver for tests.  No external dependencies."""

    def __init__(self, *, fail: bool = False) -> None:
        self._fail = fail

    def name(self) -> str:
        return "mock"

    def analysis_types(self) -> list[AnalysisType]:
        return [AnalysisType.STRUCTURAL, AnalysisType.THERMAL]

    def available(self) -> tuple[bool, str]:
        return True, "mock solver (always available)"

    def write_input(
        self,
        spec: AnalysisSpec,
        mesh_info: MeshInfo,
        work_dir: Path,
    ) -> Path:
        p = work_dir / "mock.inp"
        p.write_text("mock input\n")
        return p

    def run(self, input_path: Path, work_dir: Path) -> float:
        if self._fail:
            raise RuntimeError("Mock solver failure")
        # Write mock output
        (work_dir / "mock.out").write_text("mock output\n")
        return 0.01

    def parse_results(
        self,
        work_dir: Path,
        spec: AnalysisSpec,
    ) -> FieldResult:
        if spec.analysis_type == AnalysisType.THERMAL:
            return self._mock_thermal_result(spec)
        return self._mock_structural_result(spec)

    def _mock_structural_result(self, spec: AnalysisSpec) -> FieldResult:
        yield_mpa = spec.material.yield_strength_mpa
        max_stress = yield_mpa * 0.6  # 60% of yield → safe
        safety_factor = yield_mpa / max_stress if max_stress > 0 else 99.0

        checks = [
            AnalysisCheck(
                name="yield_check",
                status=CheckStatus.PASS,
                message=f"Max von Mises {max_stress:.1f} MPa < yield {yield_mpa:.1f} MPa",
                measured=max_stress,
                limit=yield_mpa,
                suggestion="",
            ),
        ]

        fields = [
            ScalarFieldSummary(
                field_name="von_mises_stress",
                min_val=0.0,
                max_val=max_stress,
                mean_val=max_stress * 0.3,
                unit="MPa",
                max_location_xyz=(10.0, 5.0, 0.0),
            ),
            ScalarFieldSummary(
                field_name="displacement",
                min_val=0.0,
                max_val=0.05,
                mean_val=0.02,
                unit="mm",
                max_location_xyz=(20.0, 10.0, 0.0),
            ),
        ]

        return FieldResult(
            analysis_id="mock_001",
            status=CheckStatus.PASS,
            safety_factor=round(safety_factor, 2),
            max_von_mises_mpa=round(max_stress, 2),
            max_displacement_mm=0.05,
            checks=tuple(checks),
            scalar_fields=tuple(fields),
            solver_name="mock",
            solve_time_s=0.01,
        )

    def _mock_thermal_result(self, spec: AnalysisSpec) -> FieldResult:
        """Generate plausible mock thermal result."""
        # Extract temperature BCs to make the result plausible
        t_values: list[float] = []
        max_temp_limit = 0.0
        for bc in spec.boundary_conditions:
            if bc.bc_type == "temperature":
                t_values.append(bc.value.get("temperature_k", 293.15))
            limit = bc.value.get("max_temperature_k", 0.0)
            if limit > 0:
                max_temp_limit = max(max_temp_limit, limit)

        if len(t_values) >= 2:
            t_min = min(t_values)
            t_max = max(t_values)
        elif t_values:
            t_min = t_values[0]
            t_max = t_values[0] + 50.0
        else:
            t_min = 293.15
            t_max = 373.15

        t_mean = (t_min + t_max) / 2.0
        safety_factor = max_temp_limit / t_max if max_temp_limit > 0 and t_max > 0 else 99.0

        checks = [
            AnalysisCheck(
                name="thermal_gradient",
                status=CheckStatus.PASS,
                message=(
                    f"Thermal gradient {t_max - t_min:.1f} K "
                    f"(from {t_min:.1f} K to {t_max:.1f} K)"
                ),
                measured=t_max - t_min,
            ),
        ]
        if max_temp_limit > 0:
            status = CheckStatus.PASS if t_max <= max_temp_limit else CheckStatus.FAIL
            checks.insert(0, AnalysisCheck(
                name="overtemperature",
                status=status,
                message=(
                    f"Max temperature {t_max:.1f} K vs limit {max_temp_limit:.1f} K"
                ),
                measured=t_max,
                limit=max_temp_limit,
            ))

        fields = [
            ScalarFieldSummary(
                field_name="temperature",
                min_val=round(t_min, 2),
                max_val=round(t_max, 2),
                mean_val=round(t_mean, 2),
                unit="K",
                max_location_xyz=(10.0, 5.0, 0.0),
            ),
        ]

        overall = CheckStatus.PASS
        if any(c.status == CheckStatus.FAIL for c in checks):
            overall = CheckStatus.FAIL
        elif any(c.status == CheckStatus.WARN for c in checks):
            overall = CheckStatus.WARN

        return FieldResult(
            analysis_id="mock_thermal_001",
            status=overall,
            safety_factor=round(safety_factor, 2),
            max_von_mises_mpa=0.0,
            max_displacement_mm=0.0,
            checks=tuple(checks),
            scalar_fields=tuple(fields),
            solver_name="mock",
            solve_time_s=0.01,
        )


