# ---------------------------------------------------------------------------
# ABC
# ---------------------------------------------------------------------------


class FieldSolver(ABC):
    """Base class for field-problem solvers (FEA, thermal, EM)."""

    @abstractmethod
    def name(self) -> str:
        """Unique solver identifier (e.g. 'calculix', 'elmer')."""

    @abstractmethod
    def analysis_types(self) -> list[AnalysisType]:
        """Which analysis types this solver supports."""

    @abstractmethod
    def available(self) -> tuple[bool, str]:
        """Check if the solver binary is installed.

        Returns (True, diagnostic) or (False, install_instructions).
        """

    @abstractmethod
    def write_input(
        self,
        spec: AnalysisSpec,
        mesh_info: MeshInfo,
        work_dir: Path,
    ) -> Path:
        """Write solver input files to *work_dir*.  Return the main input file path."""

    @abstractmethod
    def run(self, input_path: Path, work_dir: Path) -> float:
        """Execute the solver.  Return wall-clock time in seconds."""

    @abstractmethod
    def parse_results(
        self,
        work_dir: Path,
        spec: AnalysisSpec,
    ) -> FieldResult:
        """Parse solver output and return structured results."""

    def supports_direct_solve(self) -> bool:
        """Whether this solver supports direct in-process solve from assembled matrices."""
        return False

    def solve_direct(
        self,
        spec: AnalysisSpec,
        mesh_info: MeshInfo,
        work_dir: Path,
    ) -> FieldResult:
        """Optional in-process fast path for direct solvers."""
        raise NotImplementedError


