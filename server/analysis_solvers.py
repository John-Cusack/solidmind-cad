# CalculiX solver
# ---------------------------------------------------------------------------


class CalculiXSolver(FieldSolver):
    """CalculiX (ccx) structural FEA solver."""

    def name(self) -> str:
        return "calculix"

    def analysis_types(self) -> list[AnalysisType]:
        return [AnalysisType.STRUCTURAL]

    def available(self) -> tuple[bool, str]:
        ccx = shutil.which("ccx")
        if ccx:
            return True, f"ccx found at {ccx}"
        return False, "ccx not found. Install with: apt install calculix-ccx"

    def write_input(
        self,
        spec: AnalysisSpec,
        mesh_info: MeshInfo,
        work_dir: Path,
    ) -> Path:
        """Generate CalculiX .inp file from spec and mesh.

        Reads the meshio-generated Abaqus .inp to discover actual set names,
        then writes a main .inp that references the correct sets.
        """
        mat = spec.material

        # Convert .msh → Abaqus .inp (nodes, elements, sets)
        mesh_inp_path = self._convert_mesh_to_inp(mesh_info, work_dir)

        # Parse the full meshio output for actual set names (mesh_full.inp)
        elset_names = self._parse_set_names(mesh_inp_path)

        # Build BC face group name → meshio elset name mapping
        bc_face_groups: dict[str, str] = {}
        for i, bc in enumerate(spec.boundary_conditions):
            group_name = f"bc_{i}_{bc.bc_type}"
            # meshio sanitises names — check exact and variants
            for candidate in [group_name, group_name.replace(".", "_")]:
                if candidate in elset_names:
                    bc_face_groups[group_name] = candidate
                    break

        # Find the volume element set
        vol_elset = "volume"
        if vol_elset not in elset_names:
            # Fallback: use the first C3D4 set or any set with "vol" in name
            for name in elset_names:
                if "vol" in name.lower():
                    vol_elset = name
                    break

        # Write the main analysis .inp
        inp_path = work_dir / "analysis.inp"
        lines: list[str] = []
        lines.append("** CalculiX input generated by SolidMind CAD")
        lines.append(f"** Body: {spec.body}")
        lines.append(f"** Material: {mat.name}")
        lines.append("")

        # Include mesh
        lines.append("*INCLUDE, INPUT=mesh.inp")
        lines.append("")

        # Collect valid node IDs (nodes used by C3D4 volume elements)
        ccx_mesh = work_dir / "mesh.inp"
        valid_nodes = self._collect_volume_node_ids(ccx_mesh)

        # Generate explicit node sets from surface element sets for BCs
        nset_node_counts: dict[str, int] = {}
        for i, bc in enumerate(spec.boundary_conditions):
            group_name = f"bc_{i}_{bc.bc_type}"
            elset = bc_face_groups.get(group_name)
            if elset:
                nset_name = f"Nbc{i}"
                node_ids = self._extract_nset_from_elset(mesh_inp_path, elset)
                # Filter to only nodes that exist in the volume mesh
                if valid_nodes:
                    node_ids = [n for n in node_ids if n in valid_nodes]
                if node_ids:
                    lines.append(f"*NSET, NSET={nset_name}")
                    for j in range(0, len(node_ids), 8):
                        chunk = node_ids[j:j + 8]
                        lines.append(", ".join(str(n) for n in chunk))
                    lines.append("")
                nset_node_counts[nset_name] = len(node_ids)

        # Material definition — CalculiX uses mm/N/MPa consistent units
        lines.append(f"*MATERIAL, NAME={mat.name.upper()}")
        lines.append("*ELASTIC")
        lines.append(f"{mat.youngs_modulus_mpa}, {mat.poissons_ratio}")
        lines.append("*DENSITY")
        density_tonne_mm3 = mat.density_kg_m3 * 1e-12
        lines.append(f"{density_tonne_mm3:.6e}")
        lines.append("")

        # Assign material to volume elements
        lines.append(f"*SOLID SECTION, ELSET={vol_elset}, MATERIAL={mat.name.upper()}")
        lines.append("")

        # Boundary conditions
        lines.append("*STEP")
        lines.append("*STATIC")
        lines.append("")

        for i, bc in enumerate(spec.boundary_conditions):
            nset_name = f"Nbc{i}"
            group_name = f"bc_{i}_{bc.bc_type}"
            has_set = group_name in bc_face_groups

            if not has_set:
                lines.append(f"** WARNING: no element set found for BC {i} ({bc.bc_type})")
                lines.append("")
                continue

            if bc.bc_type == "fixed":
                lines.append(f"** BC {i}: fixed")
                lines.append(f"*BOUNDARY")
                lines.append(f"{nset_name}, 1, 3, 0.0")
            elif bc.bc_type == "force":
                lines.append(f"** BC {i}: force")
                lines.append(f"*CLOAD")
                # Load contract: force values are total force on the face set.
                # CalculiX CLOAD on an NSET applies value per node, so we divide.
                node_count = max(nset_node_counts.get(nset_name, 0), 1)
                fx = bc.value.get("fx", 0.0) / node_count
                fy = bc.value.get("fy", 0.0) / node_count
                fz = bc.value.get("fz", 0.0) / node_count
                if fx != 0:
                    lines.append(f"{nset_name}, 1, {fx}")
                if fy != 0:
                    lines.append(f"{nset_name}, 2, {fy}")
                if fz != 0:
                    lines.append(f"{nset_name}, 3, {fz}")
            elif bc.bc_type == "pressure":
                p = bc.value.get("pressure_mpa", 0.0)
                elset = bc_face_groups[group_name]
                lines.append(f"** BC {i}: pressure")
                lines.append(f"*DLOAD")
                lines.append(f"{elset}, P, {p}")
            lines.append("")

        # Output requests
        lines.append("*NODE FILE")
        lines.append("U")
        lines.append("*EL FILE")
        lines.append("S")
        lines.append("*END STEP")

        inp_path.write_text("\n".join(lines))
        return inp_path

    def _convert_mesh_to_inp(self, mesh_info: MeshInfo, work_dir: Path) -> Path:
        """Convert Gmsh .msh to CalculiX-compatible .inp format.

        Uses meshio to read the Gmsh mesh, then writes a CalculiX-native .inp
        with proper formatting (CalculiX is picky about field widths).
        Also writes the meshio Abaqus output for BC set name extraction.
        """
        full_inp = work_dir / "mesh_full.inp"
        mesh_inp = work_dir / "mesh.inp"
        try:
            import meshio
            mesh = meshio.read(mesh_info.path)
            # Write meshio Abaqus for set name parsing
            meshio.write(str(full_inp), mesh, file_format="abaqus")
            # Write CalculiX-native .inp
            self._write_ccx_native(mesh, mesh_inp)
        except ImportError:
            log.warning("meshio not installed, writing stub mesh.inp")
            mesh_inp.write_text("** meshio not available\n")
            full_inp.write_text("** meshio not available\n")
        except Exception as exc:
            log.warning("Mesh conversion failed: %s", exc)
            mesh_inp.write_text(f"** mesh conversion failed: {exc}\n")
            full_inp.write_text(f"** mesh conversion failed: {exc}\n")
        return full_inp  # return full for set parsing

    def _write_ccx_native(self, mesh: Any, out_path: Path) -> None:
        """Write a CalculiX-compatible .inp directly from meshio mesh object.

        Only includes volume elements (tetra) and all nodes.
        Uses free-format with proper spacing that CalculiX can parse.
        """
        import numpy as np

        lines: list[str] = []
        lines.append("** CalculiX mesh written by SolidMind CAD")

        # Nodes — CalculiX free format: id, x, y, z
        # CalculiX has a 132-character line limit; keep coords ≤ 20 chars each
        lines.append("*NODE, NSET=Nall")
        points = mesh.points
        for i, (x, y, z) in enumerate(points):
            nid = i + 1  # 1-based
            lines.append(f"{nid}, {x:.10g}, {y:.10g}, {z:.10g}")

        # Elements — only volume (tetra4)
        tet_cells = []
        elem_id = 1
        for cell_block in mesh.cells:
            if cell_block.type == "tetra":
                lines.append(f"*ELEMENT, TYPE=C3D4, ELSET=volume")
                for conn in cell_block.data:
                    nodes = ", ".join(str(int(n) + 1) for n in conn)  # 1-based
                    lines.append(f"{elem_id}, {nodes}")
                    elem_id += 1

        out_path.write_text("\n".join(lines) + "\n")

    def _parse_set_names(self, inp_path: Path) -> set[str]:
        """Parse *ELSET and *NSET names from a meshio-generated .inp."""
        names: set[str] = set()
        try:
            for line in inp_path.read_text().splitlines():
                upper = line.upper().strip()
                if upper.startswith("*ELSET") or upper.startswith("*NSET"):
                    for part in line.split(","):
                        part = part.strip()
                        if part.upper().startswith("ELSET=") or part.upper().startswith("NSET="):
                            name = part.split("=", 1)[1].strip()
                            names.add(name)
        except Exception:
            pass
        return names

    def _collect_volume_node_ids(self, ccx_inp: Path) -> set[int]:
        """Collect all node IDs referenced by C3D4 elements in the filtered mesh."""
        node_ids: set[int] = set()
        try:
            in_c3d4 = False
            for line in ccx_inp.read_text().splitlines():
                stripped = line.strip()
                upper = stripped.upper()
                if upper.startswith("*ELEMENT") and "TYPE=C3D4" in upper:
                    in_c3d4 = True
                    continue
                if in_c3d4 and stripped.startswith("*"):
                    in_c3d4 = False
                    continue
                if in_c3d4:
                    parts = [p.strip() for p in stripped.rstrip(",").split(",") if p.strip()]
                    # parts[0] = element ID, parts[1:] = node IDs
                    for p in parts[1:]:
                        try:
                            node_ids.add(int(p))
                        except ValueError:
                            pass
        except Exception:
            pass
        return node_ids

    def _extract_nset_from_elset(
        self,
        inp_path: Path,
        elset_name: str,
    ) -> list[int]:
        """Extract unique node IDs from a surface element set in a meshio .inp.

        Reads the *ELSET to get element IDs, then finds those elements
        in *ELEMENT blocks to collect node IDs.
        """
        text = inp_path.read_text()
        lines = text.splitlines()

        # 1. Collect element IDs from the named ELSET
        elem_ids: set[int] = set()
        in_target = False
        for line in lines:
            stripped = line.strip()
            if stripped.upper().startswith("*ELSET") and f"ELSET={elset_name}" in stripped:
                in_target = True
                continue
            if in_target:
                if stripped.startswith("*"):
                    in_target = False
                    continue
                for tok in stripped.rstrip(",").split(","):
                    tok = tok.strip()
                    if tok.isdigit():
                        elem_ids.add(int(tok))

        if not elem_ids:
            return []

        # 2. Find nodes for those elements in *ELEMENT blocks
        node_ids: set[int] = set()
        in_element = False
        for line in lines:
            stripped = line.strip()
            if stripped.upper().startswith("*ELEMENT"):
                in_element = True
                continue
            if in_element:
                if stripped.startswith("*"):
                    in_element = False
                    continue
                parts = [p.strip() for p in stripped.rstrip(",").split(",") if p.strip()]
                if not parts:
                    continue
                try:
                    eid = int(parts[0])
                except ValueError:
                    continue
                if eid in elem_ids:
                    for p in parts[1:]:
                        try:
                            node_ids.add(int(p))
                        except ValueError:
                            pass

        return sorted(node_ids)

    def run(self, input_path: Path, work_dir: Path) -> float:
        """Run ccx solver."""
        job_name = input_path.stem
        t0 = time.monotonic()
        result = subprocess.run(
            ["ccx", "-i", job_name],
            cwd=str(work_dir),
            capture_output=True,
            text=True,
            timeout=300,
        )
        elapsed = time.monotonic() - t0

        if result.returncode != 0:
            log.error("ccx failed (rc=%d): %s", result.returncode, result.stderr)
            raise RuntimeError(
                f"CalculiX failed (rc={result.returncode}): {result.stderr[:500]}"
            )

        return elapsed

    def parse_results(
        self,
        work_dir: Path,
        spec: AnalysisSpec,
    ) -> FieldResult:
        """Parse CalculiX .frd output."""
        from server.analysis_result_parser import parse_frd

        frd_path = work_dir / "analysis.frd"
        if not frd_path.exists():
            return _failed_result(
                "No .frd output file found — solver may have failed",
                spec,
            )

        return parse_frd(frd_path, spec)


