"""Field solver ABC, built-in solvers, and solver pack discovery.

Built-in solvers:
- CalculiXSolver — structural FEA (apt install calculix-ccx)
- ElmerSolver — thermal + EM (Phase 3, stub only)

Additional solvers can be installed as pip packages that register the
``solidmind.solver_packs`` entry point.
"""
